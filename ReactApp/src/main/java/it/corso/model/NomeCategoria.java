package it.corso.model;

/**
 * Enum che rappresenta i diversi nomi delle categorie di corsi disponibili nel sistema.
 */
public enum NomeCategoria {
    
    /**
     * Categoria per corsi relativi allo sviluppo Front-End.
     */
    FrontEnd,
    
    /**
     * Categoria per corsi relativi allo sviluppo Back-End.
     */
    BackEnd,
    
    /**
     * Categoria per corsi relativi allo sviluppo Full-Stack, comprendendo sia Front-End che Back-End.
     */
    FullStack,
    
    /**
     * Categoria per corsi relativi alla Cybersecurity.
     */
    Cybersecurity
}
